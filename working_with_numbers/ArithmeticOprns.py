
# Python infers the data type from the value.
# Note: For Boolean values start with capital letters.

# Assigning values to a variable.
num1 = 10
num2 = 20

addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1/num2
modulus = num1 % num2

# Note: You cannot concatenate string with a numeric type.
# To concatenate string with numeric type cast the numeric value to string as shown below.
print("The addition result is::" + str(addition))
print("The subtraction result is::" + str(subtraction))
print("The multiplication result is::" + str(multiplication))
print("The division result is::" + str(division))
print("The modulus result is::" + str(modulus))
